									<span class="badge">79.2%</span>
									<p></p>
									<p>The Games Development Process module had three in-module assessments, all based around the same brief: "A local company wishes to use simple computer games as part of their promotional strategy, and wishes to commission some games for free download from their website. They are fully open to suggestions with regards to the type and style of game produced, but it is must appeal to the casual gaming demographic". The overall objective of this Coursework was to conceptualise, pitch, design and produce an initial prototype for this game.</p>
									<p>First, we were to present our chosen game concept as a “pitch” (in 5 minutes), outlining the basic concept and showing some prototype visuals for how our game idea could look (mood boards, storyboards, example screenshots and sketches). Secondly, developing upon the concept from the pitch, we were to construct a games design document that documents the technical details of our games proposal, which could be used to give design information to a future development team. Finally, using Unity 3D, we were to construct a proof-of-concept prototype to demonstrate our game concept, and to showcase our ability with the Unity 3D engine.</p>

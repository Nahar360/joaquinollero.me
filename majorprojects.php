									<h5>Dissertation of MSc in Computer Games Technology.</h5>
									<h6>September 2017</h6>
									<span class="badge">78.0%</span>
									<p></p>
									<p>Deep Learning is a class of Machine Learning algorithms which automatically extract information from raw data by using artificial neural networks in which neurons deeper in the network naturally encode features in the data. Reinforcement Learning algorithms provide a mechanism that rewards or punishes intelligent agents so they can learn that a certain feedback related to a particular situation leads to a good or a bad state in the future. The combination of these techniques, termed Deep Reinforcement Learning Networks, offers the possibility to use a Deep Learning Neural Network to produce an approximate Reinforcement Learning value table that allows extraction of features from neurons in the hidden layers of the network.</p>
									<p>The aim of the project was to solve a pathfinding problem within an environment by implementing the Reinforcement Learning Q-Learning technique by building a Q-Table, a Q-Network and a Deep Q-Network, using the Machine Learning library TensorFlow. The results showed that the extraction of features from the hidden layers of the Deep Q-Network decreased the training time of the agent and proved the existence of encoded information regarding the best action to take from a specific state. The project attempted to make a contribution to research on Machine Learning.</p>

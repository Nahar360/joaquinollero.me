									<p></p>
									<p>The Computer Games Architecture Coursework was not only about developing a game in the context of XNA, coding in C#, but doing it with architecture concepts in mind. In this sense, developing games is not just about technology or the process, it is something that needs an architecture, which is the structure and design of a system or product, in this case, a video game. The architecture allows technology to be structured so that the game development process is supported and mediates the relationship between the hardware and the software while also supporting the separation between the game engine and the game itself.</p>
									<p>The coursework involved the implementation of the following concepts related to architecture in video game development and how they can be applied to the actual development of a game: load appropriate game assets using a resource management strategy, control the main character using an event-driven architecture, manage separately collision detection and collision response, move and animate game elements while being frame-rate independent, configuration of the game world with a data-driven approach, use of event-listeners for the scoring system and power-ups, use of serialization for a high-score table, start/end screens with a state pattern and NPC opponents controlled with a finite state machine.</p>

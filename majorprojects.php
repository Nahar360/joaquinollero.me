									<h6>June 2017 - October 2017</h6>
									<p></p>
									<p>Adapt is a system composed by a standalone Android Wear 2.0+ Smartwatch application and a Python 3.X, Flask, MongoDB, HTML5, CSS (Bootstrap), chart.js and JavaScript web application allocated in a free, external and in the cloud Heroku server.</p>
									<p>The system aims to collect data via the sensors of the Smartwatch and to send that data to the web server application, where it is stored and can be visualized or downloaded in the form of automatically prepared Python 3.X/Matlab scripts to work with the data. The Smartwatches used to test the system have been the LG G Watch R and the Moto 360 Sport running on Android Wear 2.0+. The web server application can be accessed from any device with an Internet connection (computer, smartphone, tablet...).</p>
